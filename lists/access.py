fruits = ["apple", "banana", "cherry", "grape", "melon", "tomato"]
print(fruits[0])

# negative indexing: indexing from the end
print(fruits[-1])

# index range from a to n - 1 index
print(fruits[2:5])

print(fruits[:4])

print(fruits[2:])

print(fruits[-4:-1])

if "apple" in fruits:
  print("Yes, 'apple' is in the fruits list") 