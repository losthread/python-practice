x = 10

x += 5;  print(x)   # 15
x -= 3;  print(x)   # 12
x *= 2;  print(x)   # 24
x /= 4;  print(x)   # 6.0
x //= 2; print(x)   # 3.0  (floor division)
x **= 3; print(x)   # 27.0 (exponent)
x %= 5;  print(x)   # 2.0  (modulus)

x &= 3;  print(x)   # bitwise AND
x |= 5;  print(x)   # bitwise OR
x ^= 2;  print(x)   # bitwise XOR
x >>= 1; print(x)   # right shift
x <<= 2; print(x)   # left shift